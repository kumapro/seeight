package com.java.se.eight.usingpredicates;

public class Person {
	private String name;
	private int age;
	
	
	public Person(String name, int age) {
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	@Override
	public String toString() {
		return name + " (" + age + ")";
	}

	public int compareNames(Person p1, Person p2){
		String name1 = p2.getName();
		return name1.compareTo(p2.getName());
	}
}
